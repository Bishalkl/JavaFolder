public class LocalVariable {
    public static void main(String[] args) {

        //local variable
        int a;
        float b;
        char c;
        boolean d;

        a = 12;
        b = 99.99f; // f for convert
        c = '*';
        d = true;

        //executing the value
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
